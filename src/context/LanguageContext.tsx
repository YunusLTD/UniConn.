export type TranslationKey = keyof typeof translations['en'];

type LanguageContextType = {
    language: Language;
    setLanguage: (lang: Language) => void;
    t: (key: keyof typeof translations['en'], params?: Record<string, any>) => string;
};

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
    const [language, setLanguageState] = useState<Language>('en');

    useEffect(() => {
        const loadLanguage = async () => {
            const saved = await AsyncStorage.getItem('user_language');
            if (saved && (saved === 'en' || saved === 'tr' || saved === 'ka')) {
                setLanguageState(saved as Language);
            }
        };
        loadLanguage();
    }, []);

    const setLanguage = async (lang: Language) => {
        setLanguageState(lang);
        await AsyncStorage.setItem('user_language', lang);
    };

    const t = (key: keyof typeof translations['en'], params?: Record<string, any>): string => {
        let text = translations[language][key] || translations['en'][key] || key;
        if (params) {
            Object.keys(params).forEach(paramKey => {
                text = text.replace(`{{${paramKey}}}`, String(params[paramKey]));
            });
        }
        return text;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t }}>
            {children}
        </LanguageContext.Provider>
    );
};

export const useLanguage = () => {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error('useLanguage must be used within a LanguageProvider');
    }
    return context;
};
